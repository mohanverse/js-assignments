//Declare a variable and assign string value to it and change all the string characters to capital letters using toUpperCase() method.

let lowerCase = "hye i'm in lowercase going to convert in uppercase";

console.log("Before Converted into UpperCase : " + lowerCase);
console.log("After Converted into UpperCase : " + lowerCase.toUpperCase());

/**
 * Output
 *
 * Before Converted into UpperCase : hye i'm in lowercase going to convert in uppercase
 *
 * After Converted into UpperCase : HYE I'M IN LOWERCASE GOING TO CONVERT IN UPPERCASE
 */